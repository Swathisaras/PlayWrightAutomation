/**  Topics
1. Increment and Decrement Operator
2. DOUBLE EQUAL
3. TRIPLE EQUAL
*/

let num = 10
console.log(num++)
console.log(num)
console.log(++num)
console.log("############ DOUBLE EQUAL #################")
console.log(15 == 18)    // For double equal, both the values are same, it returns false and it wont check datatype of the values
console.log(123 == "123")
console.log("Saras" == "Saras")
console.log("Saras" == "saras") 

console.log("############ TRIPLE EQUAL #################")
console.log(15 === 18) 
console.log(15 === 15) 
console.log(123 === "123")
console.log("Saras" === "saras") 
console.log("Saras" === "Saras")


